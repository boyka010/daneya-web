export const newArrivals = products.filter(p => p.badge === "New");
export const bestSellers = products.filter(p => p.badge === "Best Seller");
export const saleItems = products.filter(p => p.badge === "Flash Sale" || p.badge === "Sale");
export const trendingItems = products.filter(p => p.badge === "Trending");
export const outletProducts = products.filter(p => p.badge === "Flash Sale");

export function getProductById(id: number): Product | undefined {
  return products.find(p => p.id === id);
}

export function getProductsByCategory(category: string): Product[] {
  return products.filter(p => p.category === category);
}

export function searchProducts(query: string): Product[] {
  const q = query.toLowerCase();
  return products.filter(
    p =>
      p.name.toLowerCase().includes(q) ||
      p.nameAr?.includes(q) ||
      p.category.toLowerCase().includes(q) ||
      p.material.toLowerCase().includes(q) ||
      p.tags.some(t => t.includes(q))
  );
}
